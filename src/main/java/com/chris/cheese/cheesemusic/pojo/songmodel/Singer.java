package com.chris.cheese.cheesemusic.pojo.songmodel;

public class Singer {
    private String id;
    private String mid;
    private String name;
    private String name_highlight;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMid() {
        return mid;
    }

    public void setMid(String mid) {
        this.mid = mid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName_highlight() {
        return name_highlight;
    }

    public void setName_highlight(String name_highlight) {
        this.name_highlight = name_highlight;
    }
}
